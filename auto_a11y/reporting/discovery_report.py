    def _sanitize_filename(self, name: str) -> str:
        """Sanitize string for use in filename"""
        import re
        safe_name = re.sub(r'[<>:"/\\|?*]', '_', name)
        safe_name = re.sub(r'[_\s]+', '_', safe_name)
        safe_name = safe_name.strip('_')
        if len(safe_name) > 50:
            safe_name = safe_name[:50]
        if not safe_name:
            safe_name = 'report'
        return safe_name
